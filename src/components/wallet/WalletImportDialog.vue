<template>
  <v-dialog
    v-model="show"
    max-width="680px"
  >
    <WalletImport />
  </v-dialog>
</template>

<script>
import WalletImport from './WalletImport.vue';

export default {
  name: 'WalletImportDialog',
  components: {
    WalletImport,
  },
  props: {
    visible: Boolean,
  },
  computed: {
    show: {
      get() {
        return this.visible;
      },
      set(value) {
        if (!value) {
          this.$emit('close');
        }
      },
    },
  },
};
</script>
